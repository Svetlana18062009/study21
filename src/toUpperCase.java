public class toUpperCase {
    public static void main(String args[]) {
        String str = "I love Java";
        String result = str.toUpperCase();
        System.out.println("Result: " + result);

        str = "1 and 2 and 3";
        result = str.toUpperCase();
        System.out.println("Result: " + result);

        str = "VERTEX";
        result = str.toUpperCase();
        System.out.println("Result: " + result);
    }
}