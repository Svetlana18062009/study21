public class substring {
    public static void main(String[] args) {
        String sentence ="Обожаю язык программирования Java";
        System.out.println(sentence.substring(6));// выводит часть строки начиная с 6 го символа.
        System.out.println(sentence.substring(6,11));//выводит часть строки с 6 по 11 символы.
    }
}
